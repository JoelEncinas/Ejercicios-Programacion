    private void iniciarVentana(){
        resultado = 0;
        tfCalc.setText(Integer.toString((int) resultado));
    }
    
    private void limpiarVentana(){
        resultado = 0;
        operacion = 0;
        numeroGlobal = "";
        n1 = 0;
        n2 = 0;
        tfCalc.setText(Integer.toString((int) resultado));
    }
    
    private void operacion(){
        if (n1 != 0 && n2 != 0) {
            switch(operacion){
                case 1: resultado = sumar(n1, n2);
                    break;
                case 2: resultado = restar(n1, n2);
                    break;
                case 3: resultado = multiplicar(n1, n2);
                    break;
                case 4: resultado = dividir(n1, n2);
                    break;    
            }
            tfCalc.setText(Integer.toString((int) resultado));
        }    
        else {
            n1 = Integer.parseInt(numeroGlobal);
        }        
    }
    
    private void getNum(String n) {
        numeroGlobal += n;
        tfCalc.setText(numeroGlobal);
    } 
  
